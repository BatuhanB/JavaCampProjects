public class StringBuilder {
    public String sumStrings(String s1,String s2){
        return s1 + " " + s2 ;
    }

    public String sumStrings(String s1,String s2,String s3){
        return s1 + " " + s2 + " " + s3;
    }

    public String sumStrings(String s1,String s2,String s3,String s4){
        return s1 + " " + s2 + " " + s3 + " " + s4;
    }
}
